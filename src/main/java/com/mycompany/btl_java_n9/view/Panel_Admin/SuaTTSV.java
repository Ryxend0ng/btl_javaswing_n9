    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         SinhVien_HoSo s=new ChuyenSinhVien().docfile();
         int ch = 0;
        if(new ThemSV_con().kiemtraText(hoten, pass, ngaysinh)){
           for(int i=0;i<list.size();++i){
               if(s.getMasv().equals(list.get(i).getMasv())){
                   ch=i;
                   break;
               }
           }
            s.setHoten(hoten.getText());
            s.setPass(pass.getText());
            s.setNgaysinh(ngaysinh.getText());
            s.setDiachi((String) diachi.getSelectedItem());
            s.setKhoa((String) khoa.getSelectedItem());
            s.setLop((String) lop.getSelectedItem());
            list.set(ch, s);
            JOptionPane.showMessageDialog(this, "Cập nhật thành công! ");
            new SinhVien_AdminDTA().ghifile(list);
            jPanel1.removeAll();
            jPanel1.add(new DanhSachSV(), new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 800, 400));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

