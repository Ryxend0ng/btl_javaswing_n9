private void setMacDinh() {
        SinhVien_HoSo s=new ChuyenSinhVien().docfile();
        hoten.setText(s.getHoten());
        pass.setText(s.getPass());
        this.setDiachi(s.getDiachi());
        this.setKhoa(s.getKhoa());
        this.setLop(s.getLop());
        try {
            MaskFormatter mf=new MaskFormatter("##/##/####");
            DefaultFormatterFactory dff=new DefaultFormatterFactory(mf);
            ngaysinh.setFormatterFactory(dff);
            ngaysinh.setText(s.getNgaysinh());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Loi: "+ex);
        }
    }
